import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
        Scanner s=new Scanner(System.in);
        int A=s.nextInt();
        int B=s.nextInt();
        int C=s.nextInt();
        int X=s.nextInt();
        if(X==A || X==B || X==C){
            System.out.println("YES");
        }else
        System.out.println("NO");
	}
}